//Learned to iterate with Java for loops. Or how to run the same code multiple times.
//for ([initialization]; [condition]; [final-expression])
//initialization is executed ONCE before the loop starts.
//condition is evaluated EVERYTIME as long as it is true.
//final-expression is executed at the end of each loop iteration.
// Example
var ourArray = [];

for (var i = 0; i < 5; i++) {
  ourArray.push(i);
}

// Setup
var myArray = [];

// Only change code below this line.
for (var i = 1; i < 6; i++) {
  myArray.push(i);
}
