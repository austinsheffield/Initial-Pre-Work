//Learned how to pass values to functions with arguments.
//Important to note that variables do matter. You cannot reuse a, b after you have already used it with another function.
// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

// Only change code below this line.
function functionWithArgs(c, d) {
  console.log(c + d);
}
functionWithArgs(1 , 2);
